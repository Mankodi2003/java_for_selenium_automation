// when we want not to change value of variable we have to use "final" keyword...

public class g_Final {
    public static void main(String[] args) {  // if variable is final then write it in upper case...
        final int MYNUM = 15;
        // MYNUM = 20;              we can't change this as we declared it already with final keyword...

        System.out.println(MYNUM);
    }
}
