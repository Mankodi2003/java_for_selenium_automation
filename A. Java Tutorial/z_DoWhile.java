public class z_DoWhile{
    public static void main(String[] args) {
        int i = 0;

        do{
            System.out.println(i);
            i++;
        }
        while(i < 5);






        int x = 10;

        do{
            System.out.println("x is " + x);
            x++;
        } while (x < 5);
    }

}