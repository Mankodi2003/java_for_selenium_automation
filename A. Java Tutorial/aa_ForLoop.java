public class aa_ForLoop {
    public static void main(String[] args) {
        for(int i = 0 ; i < 10 ; i++) {
            System.out.println(i);
        }



        int sum = 0;
        for(int i = 0 ; i <= 5 ; i++){
            sum += i;
        }
        System.out.println("Sum is " + sum);
    }
}
