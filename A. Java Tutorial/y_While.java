public class y_While {
    public static void main(String[] args){
        int i = 0;

        while(i < 5){
            System.out.println(i);
            i++;
        }
        System.out.println("Completed!");



        int countdown = 3;

        while(countdown > 0) {
            System.out.println(countdown);
            countdown--;
        }
        System.out.println("Happy New Year!");
    }
}
