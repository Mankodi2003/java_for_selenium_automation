public class e_Variables {
    public static void main(String[] args){
        int sum = 10;
        System.out.println(sum);


        char ch = 'A';
        System.out.println(ch);


        boolean result = true;
        System.out.println(result);


        float desi = 54.29f;  // we have to append f after decalaring the value of floating number...
        System.out.println(desi);
 

        String myName = "Prasiddh";
        System.out.println(myName);
    }
}
